        private void table_update(){
            int c;
            
          try {
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/stats","root","");
            insert = con1.prepareStatement("select * from stats");
           
            
            ResultSet rs = insert.executeQuery();
            ResultSetMetaData Rss = rs.getMetaData();
            c = Rss.getColumnCount();
            
            DefaultTableModel Df = (DefaultTableModel)jTable1.getModel();
            
            Df.setRowCount(0);
            
            while(rs.next()){
                Vector v2 = new Vector();
                
                for(int i = 1; i <= c; i++){
                    v2.add(rs.getString("Id"));
                    v2.add(rs.getString("Name"));
                    v2.add(rs.getString("Hits"));
                    v2.add(rs.getString("Plate Apps"));
                    v2.add(rs.getString("At Bats"));
                }
                Df.addRow(v2);
                
            }
  
            } catch (ClassNotFoundException ex) {
            Logger.getLogger(BaseballStats.class.getName()).log(Level.SEVERE, null, ex);
            }
            catch (SQLException ex) {
            Logger.getLogger(BaseballStats.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        
    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        
        String name = txtName.getText();
        String hits = txtHits.getText();
        String plateApps = txtPlateApps.getText();
        String atBats = txtAtBats.getText();
        
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/stats","root","");
            if(con1 != null){
                System.out.println("Connected to database stats");
            }            
            insert = con1.prepareStatement("INSERT INTO `stats`(`Name`, `Hits`, `Plate Apps`, `At Bats`) VALUES (?,?,?,?)");
            
            

            insert.setString(1, name);
            insert.setString(2, hits);
            insert.setString(3, plateApps);
            insert.setString(4, atBats);
            insert.executeUpdate();
            
            JOptionPane.showMessageDialog(this,"Stats added");
            table_update();
            
            txtName.setText("");
            txtHits.setText("");
            txtPlateApps.setText("");
            txtAtBats.setText("");
            txtName.requestFocus();
            
            
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(BaseballStats.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch (SQLException ex) {
            Logger.getLogger(BaseballStats.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_addButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        DefaultTableModel Df = (DefaultTableModel)jTable1.getModel();
        int selectedIndex = jTable1.getSelectedRow();
        
        try {
            int id = Integer.parseInt(Df.getValueAt(selectedIndex, 0).toString());
            String name = txtName.getText();
            String hits = txtHits.getText();
            String plateApps = txtPlateApps.getText();
            String atBats = txtAtBats.getText();
            
            
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/stats","root","");
            
            insert = con1.prepareStatement("UPDATE `stats` SET `Id`=?,`Name`=?,`Hits`=?,`Plate Apps`=?,`At Bats`=? WHERE Id=?");
            insert.setInt(1, id);
            insert.setString(2, name);
            insert.setString(3, hits);
            insert.setString(4, plateApps);
            insert.setString(5, atBats);
            insert.setInt(6, id);
            insert.executeUpdate();
            
            
            JOptionPane.showMessageDialog(this,"Stats Updated ");
            table_update();
            
            txtName.setText("");
            txtHits.setText("");
            txtPlateApps.setText("");
            txtAtBats.setText("");
            txtName.requestFocus();
            
            
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(BaseballStats.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch (SQLException ex) {
            Logger.getLogger(BaseballStats.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_editButtonActionPerformed

