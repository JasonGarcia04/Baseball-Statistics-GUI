    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        DefaultTableModel Df = (DefaultTableModel)jTable1.getModel();
        int selectedIndex = jTable1.getSelectedRow();
        
        try {
            int id = Integer.parseInt(Df.getValueAt(selectedIndex, 0).toString());
            
            int dialogResult = JOptionPane.showConfirmDialog(null, "Do you want to Delete?", "Warning", JOptionPane.YES_NO_OPTION);
            
            if(dialogResult == JOptionPane.YES_OPTION){
                Class.forName("com.mysql.jdbc.Driver");
                con1 = DriverManager.getConnection("jdbc:mysql://localhost/stats","root","");

                insert = con1.prepareStatement("DELETE FROM `stats` WHERE Id=?");
                
                insert.setInt(1, id);

                insert.executeUpdate();


                JOptionPane.showMessageDialog(this,"Stats Deleted ");
                table_update();

                txtName.setText("");
                txtHits.setText("");
                txtPlateApps.setText("");
                txtAtBats.setText("");
                txtName.requestFocus();
            }

            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(BaseballStats.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch (SQLException ex) {
            Logger.getLogger(BaseballStats.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

